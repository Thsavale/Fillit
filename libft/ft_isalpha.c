#include "libft.h"

int		ft_isalpha(int c)
{
	if ((c > 0101 && c <= 0132) || (c >= 0141 && c <= 0172))
		return (1);
	else if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}
